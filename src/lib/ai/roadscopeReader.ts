// ── Types surfaced to the AI ─────────────────────────────────────────────────

/**
 * Origin of a POI returned by combined queries. The AI uses this to reason
 * about authority and freshness.
 */
export type PoiOrigin =
  /** Captured live in MeasurePRO, lives in IndexedDB. Most current. */
  | 'mp-local'
  /** POI created natively in RoadScope UI by a route surveyor. Authoritative for the SolTec taxonomy. */
  | 'roadscope-manual'
  /** POI synced INTO RoadScope from a MeasurePRO instance (you or a collaborator). */
  | 'roadscope-sync'
  /** POI auto-generated by Route Intelligence from one of the 9 SolTec sources. */
  | 'roadscope-ri';

export interface CombinedPoi {
  id: string;
  origin: PoiOrigin;
  /** RoadScope POI type string (e.g., "Bridge", "STUSA-Bridge", "STWo-PowerLine") */
  type: string;
  name?: string;
  description?: string;
  notes?: string;
  latitude: number;
  longitude: number;
  /** Vertical clearance in meters when known (NBI / MTMD / OSM maxheight). */
  verticalClearanceM?: number;
  /** Load rating in metric tons when known (NBI). */
  loadRatingMetricTons?: number;
  /** NBI condition rating 0-9 when known. */
  conditionRating?: number;
  /** Computed: bridge is critical (Poor || clearance<4m || rating<36t). */
  isCritical?: boolean;
  /** Manual or auto-assigned operational badges. */
  badges?: string[];
  /** RoadScope source label, already SolTec-branded ("SolTecUSA", "SolTecWorld", etc.). */
  sourceLabel?: string;
  /** Distance from query center in meters (set by getObstaclesNearby). */
  distanceM?: number;
  /** Survey id this POI belongs to (RoadScope side). */
  surveyId?: string;
  /** True if archived (not deleted). */
  archived?: boolean;
}

export interface RoadScopeRouteSummary {
  id: string;
  surveyId?: string;
  surveyName: string;
  surveyPhase?: 'pre-survey' | 'field' | 'customer';
  pathSampleStart?: { lat: number; lng: number };
  pathSampleEnd?: { lat: number; lng: number };
  pathLength: number;
  archived?: boolean;
}

