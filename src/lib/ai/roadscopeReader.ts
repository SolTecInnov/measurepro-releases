// ── Helpers ───────────────────────────────────────────────────────────────────

/**
 * Map an internal upstream source name (NBI, OSM, MTMD, NRN, ON511, QC511,
 * BAST, FRDG, NDW) to the SolTec brand label that the AI must use in its
 * outputs. The internal names should NEVER reach a user-visible string —
 * always pass through this function first.
 */
export function formatSourceLabel(source: string | undefined | null): string {
  if (!source) return '';
  const map: Record<string, string> = {
    OSM: 'SolTecWorld',
    NBI: 'SolTecUSA',
    MTMD: 'SolTecQC',
    NRN: 'SolTecCAN',
    ON511: 'SolTecON',
    QC511: 'SolTecQC 511',
    BAST: 'SolTecDE',
    FRDG: 'SolTecFR',
    NDW: 'SolTecNL',
  };
  return map[source] || source;
}

/**
 * Critical bridge formula. Mirrors RoadScope's own definition:
 *   Poor condition || vertical clearance < 4.0 m || operating rating < 36 t
 */
export function isBridgeCritical(opts: {
  conditionRating?: number;
  verticalClearanceM?: number;
  loadRatingMetricTons?: number;
}): boolean {
  const { conditionRating, verticalClearanceM, loadRatingMetricTons } = opts;
  if (typeof conditionRating === 'number' && conditionRating <= 4) return true;
  if (typeof verticalClearanceM === 'number' && verticalClearanceM < 4.0) return true;
  if (typeof loadRatingMetricTons === 'number' && loadRatingMetricTons < 36) return true;
  return false;
}

/**
 * NBI condition rating to semantic bucket. Mirrors RoadScope's mapping.
 *   7-9 = Good, 5-6 = Fair, 0-4 = Poor.
 */
export function conditionFromRating(rating: number | undefined | null): 'Good' | 'Fair' | 'Poor' | 'Unknown' {
  if (typeof rating !== 'number') return 'Unknown';
  if (rating >= 7) return 'Good';
  if (rating >= 5) return 'Fair';
  return 'Poor';
}

/**
 * Haversine distance in meters between two coordinates.
 */
export function haversineMeters(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371000;
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

/**
 * Bearing from point A to point B in degrees (0 = north, 90 = east).
 */
export function bearingDeg(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const φ1 = lat1 * Math.PI / 180;
  const φ2 = lat2 * Math.PI / 180;
  const Δλ = (lng2 - lng1) * Math.PI / 180;
  const y = Math.sin(Δλ) * Math.cos(φ2);
  const x = Math.cos(φ1) * Math.sin(φ2) - Math.sin(φ1) * Math.cos(φ2) * Math.cos(Δλ);
  const θ = Math.atan2(y, x);
  return ((θ * 180 / Math.PI) + 360) % 360;
}

/**
 * Returns true if `target` falls within the angular cone centered on
 * `headingDeg` with half-angle `halfAngleDeg`. Used for "what's ahead of me"
 * filtering when the user has a heading.
 */
export function isWithinHeadingCone(
  centerLat: number, centerLng: number,
  targetLat: number, targetLng: number,
  headingDeg: number,
  halfAngleDeg: number = 60,
): boolean {
  const target = bearingDeg(centerLat, centerLng, targetLat, targetLng);
  let diff = Math.abs(target - headingDeg);
  if (diff > 180) diff = 360 - diff;
  return diff <= halfAngleDeg;
}

/**
 * Bounding box (degrees) around a center point that covers ~radiusMeters in
 * every direction. Cheap pre-filter so Firestore queries don't pull the entire
 * collection. Real distance check happens client-side after.
 */
function bboxForRadius(lat: number, lng: number, radiusMeters: number) {
  const latDelta = radiusMeters / 111_320; // 1 deg lat ≈ 111.32 km
  const lngDelta = radiusMeters / (111_320 * Math.cos(lat * Math.PI / 180));
  return {
    minLat: lat - latDelta,
    maxLat: lat + latDelta,
    minLng: lng - lngDelta,
    maxLng: lng + lngDelta,
  };
}

function isLikelyArchived(d: any): boolean {
  return d?.archived === true;
}

function getDb(): Firestore | null {
  if (!db) return null;
  return db as Firestore;
}

