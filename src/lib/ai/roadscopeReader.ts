/**
 * RoadScope Read-Only Firestore Client (for the Claude AI Assistant)
 *
 * Direct Firestore reads scoped to the RoadScope collections that live alongside
 * MeasurePRO's own data in the shared `os-hub-db` Firebase project. The AI uses
 * this to surface obstacles, routes, and pre-computed Route Intelligence results
 * for the user's active or nearby surveys.
 *
 * READ-ONLY by design. The AI never writes to RoadScope through this module —
 * any AI-proposed mutations go through MeasurePRO's local POI flow first, and
 * the existing sync engine (src/lib/roadscope/syncService.ts) pushes them up to
 * RoadScope as part of the normal user-approved sync.
 *
 * Architectural notes:
 * - The Firestore client is the same one MeasurePRO already uses (db from
 *   src/lib/firebase.ts). Same project, same auth, same security rules.
 * - The 9 SolTec data sources (SolTecWorld / SolTecUSA / SolTecQC / SolTecCAN /
 *   SolTecON / SolTecQC 511 / SolTecDE / SolTecFR / SolTecNL) are all already
 *   integrated server-side by RoadScope's Route Intelligence pipeline. Their
 *   results land in `routeIntelligenceCache/`, and the imported POIs land in
 *   `surveys/{id}/pois/`. We just consume them.
 * - Brand naming rule: in any string this module surfaces to the user, ALWAYS
 *   use the SolTec labels — never OpenStreetMap, NBI, MTMD, etc.
 *
 * Schema reference (verified 2026-04-11):
 *   surveys/{id}                       — survey doc
 *   surveys/{id}/pois/                 — POIs created NATIVELY in RoadScope UI
 *                                        (full SolTec taxonomy)
 *   surveys/{id}/RoadScope-POIs/       — POIs synced FROM MeasurePRO into RS
 *                                        (MeasurePRO's collaborator data)
 *   routes/                            — TOP-LEVEL routes (NOT a subcollection)
 *   routeIntelligenceCache/{cacheKey}  — pre-computed RI analysis
 *                                        cacheKey = ri_{routeId16}_{djb2(...)}
 *   riSourceCache/{tileKey}            — server-cached NBI tiles (read seldom)
 */

